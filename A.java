public class A {

    // Instance Variable
    int number = 100;

    // Specific Method 1
    public void methodA1() {
        System.out.println("Method A1");
    }

    // Specific Method 2
    public void methodA2() {
        System.out.println("Method A2");
    }

    // Override Method
    public void display() {
        System.out.println("Display Method of Class A");
    }
}